                        <p class="title">Cagayan Valley</p>
                        <p class="desc">An administrative region in the Philippines, located in the northeastern section of Luzon Island. It is composed of five Philippine provinces: 
                            Batanes, Cagayan, Isabela, Nueva Vizcaya, and Quirino. The region hosts four chartered cities of Cauayan, Ilagan, Santiago, and Tuguegarao.
                            Most of the land area is situated on the valley between the Cordilleras and the Sierra Madre mountain ranges. The eponymous Cagayan River, 
                            the country's largest and longest, runs through the region and flows from the Caraballo Mountains and ends at Aparri. Cagayan Valley is the 
                            second largest Philippine administrative region by land area. According to a literacy survey in 2013, 97.2% of Cagayan Valley's citizens (ages 10 to 64) 
                            are functionally literate, which is the highest out of the seventeen regions of the Philippines.
                            Cagayan Valley is the large mass of land in the northeastern region of Luzon, comprising the provinces of Cagayan, Isabela, Nueva Vizcaya, Quirino, and 
                            the Batanes group of islands. It is bordered to the west by the Cordillera mountain range, to the east by the Sierra Madre, to the south by the Caraballo Mountains, 
                            and to the north by the Luzon Strait. The region contains two landlocked provinces, Quirino and Nueva Vizcaya, which are ruggedly mountainous and heavily forested. 
