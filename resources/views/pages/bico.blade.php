                    <div class="title-info">       
                        <p class="title">Bicol Region</p>
                        <p class="desc">Bicol, known formally as the Bicol Region or colloquially as Bicolandia is 
                            an administrative region of the Philippines, designated as Region V. Bicol comprises six 
                            provinces, four on the Bicol Peninsula mainland (the southeastern end of Luzon) – Albay, 
                            Camarines Norte, Camarines Sur, and Sorsogon – and the offshore island provinces of Catanduanes 
                            and Masbate. The regional center is Legazpi City and has one Independent Component City, the pilgrim 
                            city of Naga. he region is bounded by the Lamon Bay to the north, the Philippine Sea to the east, and 
                            the Sibuyan Sea and Ragay Gulf to the west. The northernmost provinces, Camarines Norte and Camarines Sur, 
                            are bordered to the west by the province of Quezon. The Bicol Region comprises the southern part of Luzon, 
                            the largest island in the Philippine archipelago. The total land area is 5.9% of the total land area of the 
                            country. Around 69.3% of the total land area is alienable and disposable while the remaining 30.7% is public 
                            forest areas. The Bicol region was known as Ibalong, variously interpreted to derive form ibalio, "to bring to 
                            the other side"; ibalon, "people from the other side" or "people who are hospitable and give visitors gifts to 
                            bring home"; or as a corruption of Gibal-ong, a sitio of Magallanes, Sorsogon where the Spaniards first landed in 
                            1567. The region comprises six provinces: Albay, Camarines Norte, Camarines Sur, Sorsogon and the island-provinces 
                            of Catanduanes and Masbate, 107 Municipalities and 3,471 Barangays. </p>
